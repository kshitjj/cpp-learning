#include <iostream>
#include <cmath>
using namespace std;

int main(){
	// getting the maximum and minimum from the two numbers
	cout << "The greater number out of 5 and 10 is " << max(5, 10) << endl;
	cout << "The smaller number out of 5 and 10 is " << min(5, 10) << endl; 
	
	//using the cmath libary
	cout << "sqrt of 9 is " << sqrt(9) << endl;
	cout << "rounding 2.6, we get " << round(2.6) << endl;
	cout << "log of 2 " << log(2) << endl;
	return 0;

}
